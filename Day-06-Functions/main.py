# Function without Parameters

def welcome():
    print("Welcome to Python")

welcome()

# Function with Parameters

def greet(name):
    print("Hello,", name)

greet("Sam")

# Function with Return Value

def add(a, b):
    return a + b

result = add(10, 20)
print("Sum:", result)

# Function to Find Square

def square(number):
    return number * number

print("Square:", square(5))
