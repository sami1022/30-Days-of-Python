# Creating Strings

name = "Python Programming"

# Indexing
print(name[0])
print(name[7])

# Slicing
print(name[0:6])
print(name[7:18])

# String Methods
print(name.upper())
print(name.lower())
print(name.replace("Python", "Java"))

# Concatenation
first_name = "Sam"
last_name = "Smith"

print(first_name + " " + last_name)

# Length of String
print("Length:", len(name))
