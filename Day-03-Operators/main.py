# Operators in Python

a = 15
b = 4

# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Floor Division:", a // b)
print("Power:", a ** b)

# Comparison Operators
print(a > b)
print(a == b)
print(a != b)

# Logical Operators
x = True
y = False

print(x and y)
print(x or y)
print(not x)
