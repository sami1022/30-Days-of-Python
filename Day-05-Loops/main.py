
# for Loop Example

for i in range(1, 6):
    print("Number:", i)

# while Loop Example

count = 1

while count <= 5:
    print("Count:", count)
    count += 1

# break Statement

for i in range(1, 10):
    if i == 5:
        break
    print(i)

# continue Statement

for i in range(1, 6):
    if i == 3:
        continue
    print(i)
